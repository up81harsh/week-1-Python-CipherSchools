#slicing /selecting subsequences
lang = "python"
#syntax - [start arguement: stop arguement:step]
print("harsh"[2:5])     #output= rsh
print("harsh"[2:5:1])   #output= rsh
print("harsh"[2:5:2])   #output= rh
print("harsh"[0::2])    #output= hrh
print("harsh"[8::-1])   #output = hsrah
print("harsh"[::-1])    #output = hsrah (TRICK)