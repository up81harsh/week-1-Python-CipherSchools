#slicing /selecting subsequences
lang = "python"
# print(lang[4])
#syntax - [start arguement: stop arguement]
print(lang[0:2])   #output = py
print(lang[2:4])   #output = th
print(lang[-3:6])  #output = hon
print(lang[:])     #output = python
print(lang[1:])    #output = ython
print(lang[:3])    #output = pyt
