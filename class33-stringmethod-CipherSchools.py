#string methods part one
name = "HArsh"

#1. len() function
print(len(name))
#     OR
length = len(name)
print(length)

#2. lower() method : it will change all the letters into lowercase letters
print(name.lower())

#3. upper() method : it will convert all the lowercase into uppercase
print(name.upper())

#4. title() method : it will change first letter to uppercase . rest will be same
print(name.title())

#5. count() method : it will count total number of letters present
print(name.count("N"))